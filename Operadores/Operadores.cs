using System;
					
public class Operadores
{
	public static void Main()
	{
		double a = 5, b = 2;
		double resultado;
		
		resultado = a + b;
		Console.WriteLine(resultado);
		 
		resultado = a - b;
		Console.WriteLine(resultado);
		
		resultado = a * b;
		Console.WriteLine(resultado);
		
		resultado = a / b;
		Console.WriteLine(resultado);
		
		resultado = a % b;
		Console.WriteLine(resultado);
	}
}
